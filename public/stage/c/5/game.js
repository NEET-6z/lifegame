class StageC5Evaluator extends GameEvaluator {
  setgameInfo() {
    super.setgameInfo();;
    this.gameInfo["complete"] = 0;
  }

  evaluateTurn() {
    if (this.cmis0() && this.board.getValueCount(2) === 0) {
      this.gameInfo["complete"] = 1;
    }
    if (this.cmis0() && this.gameInfo["turn"] >= 100) {
      this.gameInfo["complete"] = -1;
    }

    return super.evaluateTurn();
  }
}

